function SearchableCombobox({
  options,
  value,
  onValueChange,
  placeholder,
  searchPlaceholder,
  testId,
}: {
  options: { value: string; label: string }[];
  value: string;
  onValueChange: (value: string) => void;
  placeholder: string;
  searchPlaceholder: string;
  testId: string;
}) {
  const [open, setOpen] = useState(false);
  const selectedOption = options.find((opt) => opt.value === value);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className="w-full justify-between font-normal"
          data-testid={testId}
        >
          {selectedOption ? selectedOption.label : placeholder}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[--radix-popover-trigger-width] p-0" align="start">
        <Command>
          <CommandInput placeholder={searchPlaceholder} />
          <CommandList>
            <CommandEmpty>No results found.</CommandEmpty>
            <CommandGroup>
              {options.map((option) => (
                <CommandItem
                  key={option.value}
                  value={option.label}
                  onSelect={() => {
                    onValueChange(option.value);
                    setOpen(false);
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      value === option.value ? "opacity-100" : "opacity-0"
                    )}
                  />
                  {option.label}
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
}

type PolicyWarningType = "short-notice" | "domestic-advisory" | "international-advisory" | null;

interface PolicyWarning {
  type: PolicyWarningType;
  message: string;
  severity: "warning" | "info";
}

function calculatePolicyWarning(travelDate: string, destination: string): PolicyWarning | null {
  if (!travelDate || !destination) return null;
  
  const today = startOfDay(new Date());
  const departure = startOfDay(parseISO(travelDate));
  const daysUntilDeparture = differenceInCalendarDays(departure, today);
  
  if (daysUntilDeparture < 0) return null;
  
  const isDomestic = destination === "US";
  
  if (daysUntilDeparture < 14) {
    return {
      type: "short-notice",
      message: "Short-notice travel: This trip is less than 14 days away. Carta policy requires additional Executive Leader approval for travel booked within 14 days of departure.",
      severity: "warning",
    };
  }
  
  if (isDomestic && daysUntilDeparture < 21) {
    return {
      type: "domestic-advisory",
      message: "Carta recommends booking domestic travel 21+ days in advance.",
      severity: "info",
    };
  }
  
  if (!isDomestic && daysUntilDeparture < 35) {
    return {
      type: "international-advisory",
      message: "Carta recommends booking international travel 35+ days in advance.",
      severity: "info",
    };
  }
  
  return null;
}

export function EntryForm({ onSubmit, defaultDestination, defaultCitizenship }: EntryFormProps) {
  const validatedDestination = defaultDestination && 
    destinationOptions.some(opt => opt.value === defaultDestination) 
      ? defaultDestination 
      : "";
  
  const validatedCitizenship = defaultCitizenship && 
    citizenshipOptions.some(opt => opt.value === defaultCitizenship) 
      ? defaultCitizenship 
      : "";
  
  const form = useForm<EntryFormData>({
    resolver: zodResolver(formSchema),
    defaultValues: {
      citizenship: validatedCitizenship,
      destination: validatedDestination,
      durationDays: 7,
      travelDate: "",
      isUSEmployerSponsored: false,
    },
  });

  const travelDate = form.watch("travelDate");
  const destination = form.watch("destination");
  const policyWarning = calculatePolicyWarning(travelDate, destination);

  const handleSubmit = async (data: EntryFormData) => {
    await onSubmit({ ...data, purpose: "BUSINESS" });
  };

  return (
    <Form {...form}>
      <form onSubmit={form.handleSubmit(handleSubmit)} className="space-y-6">
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          <FormField
            control={form.control}
            name="citizenship"
            render={({ field }) => (
              <FormItem className="flex flex-col">
                <FormLabel>Citizenship</FormLabel>
                <SearchableCombobox
                  options={citizenshipOptions}
                  value={field.value}
                  onValueChange={field.onChange}
                  placeholder="Search country..."
                  searchPlaceholder="Type to search..."
                  testId="select-citizenship"
                />
                <FormDescription className="text-xs">
                  Your passport nationality
                </FormDescription>
                <FormMessage />
              </FormItem>
            )}
          />

          <FormField
            control={form.control}
            name="destination"
            render={({ field }) => (
              <FormItem className="flex flex-col">
                <FormLabel>Destination</FormLabel>
                <SearchableCombobox
                  options={destinationOptions}
                  value={field.value}
                  onValueChange={field.onChange}
                  placeholder="Select office..."
                  searchPlaceholder="Search destination..."
                  testId="select-destination"
                />
                <FormDescription className="text-xs">
                  Supported Carta office locations
                </FormDescription>
                <FormMessage />
              </FormItem>
            )}
          />
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          <FormField
            control={form.control}
            name="travelDate"
            render={({ field }) => (
              <FormItem className="flex flex-col">
                <FormLabel className="flex items-center gap-2">
                  <CalendarIcon className="w-3.5 h-3.5 text-muted-foreground" />
                  Travel Date
                </FormLabel>
                <Popover>
                  <PopoverTrigger asChild>
                    <FormControl>
                      <Button
                        variant="outline"
                        className={cn(
                          "w-full justify-start text-left font-normal",
                          !field.value && "text-muted-foreground"
                        )}
                        data-testid="input-travel-date"
                      >
                        <CalendarIcon className="mr-2 h-4 w-4" />
                        {field.value ? format(new Date(field.value), "PPP") : "Pick a date"}
                      </Button>
                    </FormControl>
                  </PopoverTrigger>
                  <PopoverContent className="w-auto p-0" align="start">
                    <CalendarComponent
                      mode="single"
                      selected={field.value ? new Date(field.value) : undefined}
                      onSelect={(date) => {
                        if (date) {
                          field.onChange(format(date, "yyyy-MM-dd"));
                        }
                      }}
                      disabled={(date) => date < new Date()}
                      initialFocus
                    />
                  </PopoverContent>
                </Popover>
                <FormDescription className="text-xs">
                  Must be a future date
                </FormDescription>
                <FormMessage />
              </FormItem>
            )}
          />

          <FormField
            control={form.control}
            name="durationDays"
            render={({ field }) => (
              <FormItem>
                <FormLabel className="flex items-center gap-2">
                  <Clock className="w-3.5 h-3.5 text-muted-foreground" />
                  Duration
                </FormLabel>
                <FormControl>
                  <Input
                    type="number"
                    min={1}
                    max={365}
                    data-testid="input-duration"
                    {...field}
                  />
                </FormControl>
                <FormDescription className="text-xs">
                  Number of days
                </FormDescription>
                <FormMessage />
              </FormItem>
            )}
          />
        </div>

        {policyWarning && (
          <div 
            className={cn(
              "flex items-start gap-3 rounded-lg p-4 text-sm",
              policyWarning.severity === "warning" 
                ? "bg-amber-50 dark:bg-amber-950/30 border border-amber-200 dark:border-amber-800" 
                : "bg-blue-50 dark:bg-blue-950/30 border border-blue-200 dark:border-blue-800"
            )}
            data-testid="policy-warning"
          >
            {policyWarning.severity === "warning" ? (
              <AlertTriangle className="h-5 w-5 text-amber-600 dark:text-amber-400 flex-shrink-0 mt-0.5" />
            ) : (
              <Info className="h-5 w-5 text-blue-600 dark:text-blue-400 flex-shrink-0 mt-0.5" />
            )}
            <p className={cn(
              "leading-relaxed",
              policyWarning.severity === "warning" 
                ? "text-amber-800 dark:text-amber-200" 
                : "text-blue-800 dark:text-blue-200"
            )}>
              {policyWarning.message}
            </p>
          </div>
        )}

        <FormField
          control={form.control}
          name="isUSEmployerSponsored"
          render={({ field }) => (
            <FormItem className="flex flex-row items-center justify-between rounded-xl border border-border/50 p-4 bg-muted/30">
              <div className="space-y-1 pr-4">
                <FormLabel className="text-sm font-medium">U.S. employer-sponsored visa holder</FormLabel>
                <FormDescription className="text-xs leading-relaxed">
                  Enable if you hold an H-1B, L-1, O-1, or similar U.S. work visa sponsored by Carta. This may affect re-entry requirements and visa interview needs.
                </FormDescription>
              </div>
              <FormControl>
                <Switch
                  checked={field.value}
                  onCheckedChange={field.onChange}
                  data-testid="switch-us-sponsored"
                />
              </FormControl>
            </FormItem>
          )}
        />

        <Button
          type="submit"
          className="w-full"
          size="lg"
          disabled={form.formState.isSubmitting}
          data-testid="button-check-requirements"
        >
          {form.formState.isSubmitting ? "Checking..." : "Check Entry Requirements"}
        </Button>
      </form>
    </Form>
  );
}
